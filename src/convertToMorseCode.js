function lettersToMorseCode(str){
    str = str.toUpperCase();
    let output = "";
    var tableData = {
     "A" : ".-",   "K" : "-.-",  "U" : "..-",   "5" : ".....",  "," : "--..--", "$" : "...-..-",
     "B" : "-...", "L" : ".-..", "V" : "...-",  "6" : "-....",  "?" : "..--..", " " : "/",
     "C" : "-.-.", "M" : "--",   "W" : ".--",   "7" : "--...",  "(" : "-.--.",
     "D" : "-..",  "N" : "-.",   "X" : "-..-",  "8" : "---..",  ")" : "-.--.-",
     "E" : ".",    "O" : "---",  "Y" : "-.--",  "9" : "----.",  "-" : "-....-",
     "F" : "..-.", "P" : ".--.", "Z" : "--..",  "0" : "-----",  "\"" : ".-..-.",
     "G" : "--.",  "Q" : "--.-", "1" : ".----", "/" : "-..-.",  "_" : "..--.-",
     "H" : "....", "R" : ".-.",  "2" : "..---", "+" : ".-.-.",  "'" : ".----.",
     "I" : "..",   "S" : "...",  "3" : "...--", "=" : "-...-",  ":" : "---...",
     "J" : ".---", "T" : "-",    "4" : "....-", "." : ".-.-.-", ";" : "-.-.-."
    };
    for(var i = 0; i < str.length; i++){
        var temp = tableData[str.charAt(i)];
        if(temp != null){
            output += temp + " ";
        }
        else{
            output += "  ";
        }
    }
    return output;
    
}
console.log(lettersToMorseCode("Hi there"));

module.exports = {lettersToMorseCode};